export default function LegalDocumentPage() {
  const { id } = useParams<{ id: string }>();
  const navigate = useNavigate();
  const doc = id ? documents[id] : null;

  if (!doc) {
    return (
      <div className="min-h-screen bg-gray-50 flex items-center justify-center">
        <div className="text-center">
          <p className="text-gray-500">Документ не найден</p>
          <button
            onClick={() => navigate('/legal/documents')}
            className="mt-4 text-blue-600 font-medium"
          >
            Назад к списку
          </button>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-white pb-20">
      {/* Header */}
      <header className="px-4 py-4 border-b border-gray-200 sticky top-0 z-10 bg-white">
        <div className="flex items-center gap-3">
          <button
            onClick={() => navigate(-1)}
            className="text-blue-600 font-medium"
          >
            ← Назад
          </button>
          <h1 className="text-lg font-bold text-gray-900 flex-1">{doc.title}</h1>
        </div>
      </header>

      {/* Content */}
      <div className="px-4 py-6 max-w-2xl mx-auto">
        <div className="prose prose-sm max-w-none">
          {doc.content.split('\n\n').map((paragraph, idx) => {
            if (paragraph.startsWith('#')) {
              return (
                <h2 key={idx} className="text-xl font-bold text-gray-900 mt-6 mb-3">
                  {paragraph.replace(/^#\s*/, '')}
                </h2>
              );
            }
            if (paragraph.startsWith('##')) {
              return (
                <h3 key={idx} className="text-lg font-semibold text-gray-800 mt-4 mb-2">
                  {paragraph.replace(/^##\s*/, '')}
                </h3>
              );
            }
            return (
              <p key={idx} className="text-sm text-gray-700 leading-relaxed mb-3">
                {paragraph}
              </p>
            );
          })}
        </div>

        {/* Кнопка принятия */}
        <div className="mt-8 pt-6 border-t border-gray-200">
          <button
            className="w-full bg-blue-600 text-white rounded-xl py-4 px-6 font-semibold active:scale-95 transition-transform shadow-lg"
            onClick={() => {
              // Здесь будет вызов API для сохранения даты принятия
              alert('Документ принят!');
              navigate('/legal/documents');
            }}
          >
            Принимаю условия
          </button>
          <p className="text-xs text-gray-500 text-center mt-3">
            Нажимая кнопку, вы подтверждаете, что ознакомились с документом
          </p>
        </div>
      </div>
    </div>
  );
}
